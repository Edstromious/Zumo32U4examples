const uint8_t remoteRight = 0x0A;

const uint8_t remote0 = 0x00;
const uint8_t remoteDown = 0x0D;
const uint8_t remoteBack = 0x0E;

const uint8_t remote1 = 0x01;
const uint8_t remote2 = 0x02;
